package JavaWhile;

//create class JavaWhile
public class JavaWhile {

	//Main method
	public static void main(String[] args)
	{
		//declare integer value
		int x= -10;
		while(x<=0)
		{
			System.out.println("x=" + x);
			//x++;
			//x=x+1;
			x +=1;
		}
	}

}
